import java.util.Scanner;

public class Main2742 {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        int count = sc.nextInt();
        sc.close();

        for (int i = 0; i < count; i++) {
            System.out.print((i+1));
            System.out.println();
        }
    }




}
